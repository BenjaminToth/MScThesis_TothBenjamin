from __future__ import annotations

import json
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, Iterable, List, Sequence, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset

from models.WGANGP.wgangp import EEGGenerator


@dataclass(frozen=True)
class _SyntheticWindowIndex:
    label_mask: int


def _zscore_per_channel(x: torch.Tensor, *, eps: float = 1e-6) -> torch.Tensor:
    mu = x.mean(dim=1, keepdim=True)
    sigma = x.std(dim=1, keepdim=True, unbiased=False)
    return (x - mu) / (sigma + eps)


class WGANGPSyntheticMajorClassDataset(Dataset):
    """Synthetic EEG windows generated by per-class trained WGAN-GP generators.

    Returns:
      - x: FloatTensor (C, T)
      - y: FloatTensor (n_classes,) multi-hot, single positive class

    Notes:
      - This dataset pre-samples latent vectors on init to keep samples fixed.
      - Generators are loaded lazily per DataLoader worker.
    """

    def __init__(
        self,
        *,
        wgangp_root: Path | str,
        classes: Sequence[str],
        n_per_class: int,
        label_names: Sequence[str],
        expected_sfreq: float,
        expected_window_sec: float,
        normalize: bool,
        seed: int,
        device: str | torch.device = "cpu",
    ) -> None:
        self.wgangp_root = Path(wgangp_root)
        self.classes = tuple(str(c).strip().lower() for c in classes)
        self.n_per_class = int(n_per_class)
        if self.n_per_class <= 0:
            raise ValueError("n_per_class must be > 0")

        self.label_names: Tuple[str, ...] = tuple(str(x).strip().lower() for x in label_names)
        self.label_to_idx: Dict[str, int] = {n: i for i, n in enumerate(self.label_names)}

        self.expected_sfreq = float(expected_sfreq)
        self.expected_window_sec = float(expected_window_sec)
        self.expected_n_samples = int(round(self.expected_sfreq * self.expected_window_sec))

        self.normalize = bool(normalize)
        self.seed = int(seed)
        self.device = torch.device(device)
        if self.device.type != "cpu":
            self.device = torch.device("cpu")

        missing_labels = [c for c in self.classes if c not in self.label_to_idx]
        if missing_labels:
            raise ValueError(f"Requested synthetic classes missing from label_names: {missing_labels}")

        self._per_class_settings: Dict[str, dict] = {}
        self._per_class_ckpt: Dict[str, Path] = {}
        self._per_class_latents: Dict[str, np.ndarray] = {}

        missing_artifacts: List[str] = []
        for ci, class_name in enumerate(self.classes):
            out_dir = self.wgangp_root / class_name
            settings_path = out_dir / "settings.json"
            ckpt_path = out_dir / "generator_last.pt"
            if not settings_path.exists() or not ckpt_path.exists():
                missing_artifacts.append(str(out_dir))
                continue

            settings = json.loads(settings_path.read_text(encoding="utf-8"))
            self._validate_settings(class_name, settings)

            self._per_class_settings[class_name] = settings
            self._per_class_ckpt[class_name] = ckpt_path

            latent_dim = int(settings.get("latent_dim", 128))
            rng = np.random.default_rng(int(self.seed) + 1337 * (ci + 1))
            z = rng.standard_normal(size=(self.n_per_class, latent_dim), dtype=np.float32)
            self._per_class_latents[class_name] = z

        if missing_artifacts:
            raise FileNotFoundError(
                "Missing saved WGAN-GP artifacts for some classes. Expected per-class folders containing "
                "settings.json and generator_last.pt under wgangp_root. Missing: "
                + ", ".join(missing_artifacts)
            )

        self._generators: Dict[str, EEGGenerator] | None = None

    def _validate_settings(self, class_name: str, settings: dict) -> None:
        sfreq = float(settings.get("sfreq", -1.0))
        window_sec = float(settings.get("window_sec", -1.0))
        if not np.isclose(sfreq, self.expected_sfreq, atol=1e-6):
            raise ValueError(
                f"WGAN-GP settings for class '{class_name}' have sfreq={sfreq}, "
                f"but classifier expects sfreq={self.expected_sfreq}."
            )
        if not np.isclose(window_sec, self.expected_window_sec, atol=1e-9):
            raise ValueError(
                f"WGAN-GP settings for class '{class_name}' have window_sec={window_sec}, "
                f"but classifier expects window_sec={self.expected_window_sec}."
            )

    def _ensure_generators_loaded(self) -> None:
        if self._generators is not None:
            return

        gens: Dict[str, EEGGenerator] = {}
        for class_name in self.classes:
            settings = self._per_class_settings[class_name]
            latent_dim = int(settings.get("latent_dim", 128))
            output_activation = str(settings.get("output_activation", "linear")).strip().lower()

            gen = EEGGenerator(
                latent_dim=latent_dim,
                n_channels=17,
                n_samples=int(self.expected_n_samples),
                base_channels=256,
                base_length=None,
                output_activation=output_activation,
            ).to(self.device)

            state = torch.load(self._per_class_ckpt[class_name], map_location=self.device)
            gen.load_state_dict(state)
            gen.eval()
            gens[class_name] = gen

        self._generators = gens

    def __len__(self) -> int:
        return len(self.classes) * self.n_per_class

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        if idx < 0 or idx >= len(self):
            raise IndexError(idx)

        class_i = int(idx) // self.n_per_class
        within = int(idx) % self.n_per_class
        class_name = self.classes[class_i]

        self._ensure_generators_loaded()
        assert self._generators is not None

        z_np = self._per_class_latents[class_name][within]
        z = torch.from_numpy(z_np[None, :]).to(self.device)

        gen = self._generators[class_name]
        with torch.no_grad():
            x = gen(z)[0].to(dtype=torch.float32, device="cpu")  # (C, T)

        if self.normalize:
            x = _zscore_per_channel(x)

        y = torch.zeros((len(self.label_names),), dtype=torch.float32)
        y[self.label_to_idx[class_name]] = 1.0

        return x, y


class AugmentedTrainDataset(Dataset):
    """Concatenate a real EEGWindowDataset with a synthetic dataset.

    This wrapper exposes `_index` with `label_mask` for compatibility with
    `training/train.py::_compute_repeat_factor_weights`.
    """

    def __init__(
        self,
        *,
        real_ds: Dataset,
        synthetic_ds: Dataset,
        label_names: Sequence[str],
        synthetic_label_masks: Sequence[int],
        real_index: Sequence[object],
        label_to_idx: Dict[str, int],
    ) -> None:
        self.real_ds = real_ds
        self.synthetic_ds = synthetic_ds

        self.label_names: Tuple[str, ...] = tuple(str(x).strip().lower() for x in label_names)
        self.label_to_idx = dict(label_to_idx)

        self._index: List[object] = list(real_index)
        self._index.extend(_SyntheticWindowIndex(int(m)) for m in synthetic_label_masks)

    def __len__(self) -> int:
        return len(self.real_ds) + len(self.synthetic_ds)

    def __getitem__(self, idx: int):
        n_real = len(self.real_ds)
        if int(idx) < n_real:
            return self.real_ds[int(idx)]
        return self.synthetic_ds[int(idx) - n_real]


def build_major_class_synthetic_augmentation(
    *,
    real_train_ds: object,
    wgangp_root: Path | str,
    major_classes: Sequence[str],
    n_per_class: int,
    sfreq: float,
    window_sec: float,
    normalize: bool,
    seed: int,
) -> AugmentedTrainDataset:
    """Factory that wraps the provided training dataset with major-class GAN samples."""

    if not hasattr(real_train_ds, "label_names") or not hasattr(real_train_ds, "label_to_idx"):
        raise TypeError("real_train_ds must expose label_names and label_to_idx")
    if not hasattr(real_train_ds, "_index"):
        raise TypeError("real_train_ds must expose _index (window index with label_mask)")

    label_names = tuple(getattr(real_train_ds, "label_names"))
    label_to_idx = dict(getattr(real_train_ds, "label_to_idx"))

    synthetic_ds = WGANGPSyntheticMajorClassDataset(
        wgangp_root=wgangp_root,
        classes=major_classes,
        n_per_class=int(n_per_class),
        label_names=label_names,
        expected_sfreq=float(sfreq),
        expected_window_sec=float(window_sec),
        normalize=bool(normalize),
        seed=int(seed),
        device="cpu",
    )

    masks: List[int] = []
    for cn in (str(c).strip().lower() for c in major_classes):
        if cn not in label_to_idx:
            raise ValueError(f"Major class '{cn}' not in training label_names")
        bit = 1 << int(label_to_idx[cn])
        masks.extend([bit] * int(n_per_class))

    return AugmentedTrainDataset(
        real_ds=real_train_ds,
        synthetic_ds=synthetic_ds,
        label_names=label_names,
        synthetic_label_masks=masks,
        real_index=list(getattr(real_train_ds, "_index")),
        label_to_idx=label_to_idx,
    )

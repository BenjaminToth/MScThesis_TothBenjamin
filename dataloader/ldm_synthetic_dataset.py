from __future__ import annotations

import json
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Sequence, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset

from dataloader.gan_synthetic_dataset import AugmentedTrainDataset
from models.LDM import EEGLatentDiffusion


@dataclass(frozen=True)
class _LDMClassArtifacts:
    settings: dict
    vae_ckpt: Path
    unet_ckpt: Path


def _zscore_per_channel(x: torch.Tensor, *, eps: float = 1e-6) -> torch.Tensor:
    mu = x.mean(dim=1, keepdim=True)
    sigma = x.std(dim=1, keepdim=True, unbiased=False)
    return (x - mu) / (sigma + eps)


class LDMSyntheticMajorClassDataset(Dataset):
    """Synthetic EEG windows generated by per-class trained LDM checkpoints.

    Returns:
      - x: FloatTensor (C, T)
      - y: FloatTensor (n_classes,) multi-hot, single positive class

    Notes:
      - This dataset is deterministic per (seed, idx) by reseeding torch RNG in __getitem__.
      - Models are loaded lazily on first __getitem__ in the current process.
      - If generating on CUDA, this dataset should be used with DataLoader num_workers=0.

            Performance:
                - On-the-fly diffusion sampling is expensive (UNet forward per inference step).
                - For large n_per_class (e.g., 10,000), this class automatically enables a disk-backed
                    cache under <ldm_root>/_ldm_cache/ so each synthetic index is generated once per run
                    configuration, then loaded quickly on subsequent accesses/epochs.
    """

    def __init__(
        self,
        *,
        ldm_root: Path | str,
        classes: Sequence[str],
        n_per_class: int,
        label_names: Sequence[str],
        expected_sfreq: float,
        expected_window_sec: float,
        normalize: bool,
        seed: int,
        device: str | torch.device,
        num_inference_steps_override: int | None = None,
        enable_cache: bool | None = None,
    ) -> None:
        self.ldm_root = Path(ldm_root)
        self.classes = tuple(str(c).strip().lower() for c in classes)
        self.n_per_class = int(n_per_class)
        if self.n_per_class <= 0:
            raise ValueError("n_per_class must be > 0")

        self.label_names: Tuple[str, ...] = tuple(str(x).strip().lower() for x in label_names)
        self.label_to_idx: Dict[str, int] = {n: i for i, n in enumerate(self.label_names)}

        self.expected_sfreq = float(expected_sfreq)
        self.expected_window_sec = float(expected_window_sec)
        self.expected_n_samples = int(round(self.expected_sfreq * self.expected_window_sec))

        self.normalize = bool(normalize)
        self.seed = int(seed)
        self.device = torch.device(device)

        if num_inference_steps_override is not None:
            n_steps = int(num_inference_steps_override)
            if n_steps <= 0:
                raise ValueError("num_inference_steps_override must be > 0")
            self.num_inference_steps_override = n_steps
        else:
            self.num_inference_steps_override = None

        missing_labels = [c for c in self.classes if c not in self.label_to_idx]
        if missing_labels:
            raise ValueError(f"Requested synthetic classes missing from label_names: {missing_labels}")

        self._artifacts: Dict[str, _LDMClassArtifacts] = {}
        missing_artifacts: List[str] = []

        for class_name in self.classes:
            out_dir = self.ldm_root / class_name
            settings_path = out_dir / "settings.json"
            vae_ckpt = out_dir / "vae_last.pt"
            unet_ckpt = out_dir / "unet_last.pt"

            if not settings_path.exists() or not vae_ckpt.exists() or not unet_ckpt.exists():
                missing_artifacts.append(str(out_dir))
                continue

            settings = json.loads(settings_path.read_text(encoding="utf-8"))
            self._validate_settings(class_name, settings)

            self._artifacts[class_name] = _LDMClassArtifacts(
                settings=settings,
                vae_ckpt=vae_ckpt,
                unet_ckpt=unet_ckpt,
            )

        if missing_artifacts:
            raise FileNotFoundError(
                "Missing saved LDM artifacts for some classes. Expected per-class folders containing "
                "settings.json, vae_last.pt, and unet_last.pt under ldm_root. Missing: "
                + ", ".join(missing_artifacts)
            )

        self._models: Dict[str, EEGLatentDiffusion] | None = None

        if enable_cache is None:
            enable_cache = self.n_per_class >= 2000
        self.enable_cache = bool(enable_cache)
        self.cache_root = (self.ldm_root / "_ldm_cache") if self.enable_cache else None
        self._cache_x: Dict[str, np.memmap] = {}
        self._cache_done: Dict[str, np.memmap] = {}
        self._n_steps_by_class: Dict[str, int] = {}
        for class_name in self.classes:
            if self.num_inference_steps_override is not None:
                self._n_steps_by_class[class_name] = int(self.num_inference_steps_override)
            else:
                self._n_steps_by_class[class_name] = int(self._artifacts[class_name].settings.get("num_inference_steps", 50))

    def _validate_settings(self, class_name: str, settings: dict) -> None:
        sfreq = float(settings.get("sfreq", -1.0))
        window_sec = float(settings.get("window_sec", -1.0))
        if not np.isclose(sfreq, self.expected_sfreq, atol=1e-6):
            raise ValueError(
                f"LDM settings for class '{class_name}' have sfreq={sfreq}, "
                f"but classifier expects sfreq={self.expected_sfreq}."
            )
        if not np.isclose(window_sec, self.expected_window_sec, atol=1e-9):
            raise ValueError(
                f"LDM settings for class '{class_name}' have window_sec={window_sec}, "
                f"but classifier expects window_sec={self.expected_window_sec}."
            )

    def _ensure_models_loaded(self) -> None:
        if self._models is not None:
            return

        models: Dict[str, EEGLatentDiffusion] = {}

        for class_name in self.classes:
            art = self._artifacts[class_name]
            s = art.settings

            latent_channels = int(s.get("latent_channels", 32))
            vae_base_channels = int(s.get("vae_base_channels", 128))
            vae_downsample_factor = int(s.get("vae_downsample_factor", 16))
            unet_base_channels = int(s.get("unet_base_channels", 128))
            diffusion_timesteps = int(s.get("diffusion_timesteps", 1000))
            diffusion_beta_schedule = str(s.get("diffusion_beta_schedule", "cosine"))
            latent_scale = float(s.get("latent_scale", 1.0))

            model = EEGLatentDiffusion(
                n_channels=17,
                n_samples=int(self.expected_n_samples),
                latent_channels=latent_channels,
                vae_base_channels=vae_base_channels,
                vae_downsample_factor=vae_downsample_factor,
                unet_base_channels=unet_base_channels,
                diffusion_timesteps=diffusion_timesteps,
                diffusion_beta_schedule=diffusion_beta_schedule,
                latent_scale=latent_scale,
            ).to(self.device)

            vae_state = torch.load(art.vae_ckpt, map_location=self.device)
            unet_state = torch.load(art.unet_ckpt, map_location=self.device)
            model.vae.load_state_dict(vae_state)
            model.unet.load_state_dict(unet_state)
            model.eval()

            models[class_name] = model

        self._models = models

    def __len__(self) -> int:
        return len(self.classes) * self.n_per_class

    def _cache_paths(self, class_name: str) -> tuple[Path, Path, Path]:
        assert self.cache_root is not None

        n_steps = int(self._n_steps_by_class[class_name])
        norm_tag = "norm" if self.normalize else "raw"
        shape_tag = f"c17_t{int(self.expected_n_samples)}"
        run_tag = f"seed{int(self.seed)}_steps{n_steps}_{shape_tag}_{norm_tag}"

        class_dir = self.cache_root / class_name
        x_path = class_dir / f"x_{run_tag}_f16.npy"
        done_path = class_dir / f"done_{run_tag}_u8.npy"
        meta_path = class_dir / f"meta_{run_tag}.json"
        return x_path, done_path, meta_path

    def _ensure_cache_for_class(self, class_name: str) -> None:
        if not self.enable_cache:
            return
        assert self.cache_root is not None
        if class_name in self._cache_x and class_name in self._cache_done:
            return

        x_path, done_path, meta_path = self._cache_paths(class_name)
        x_path.parent.mkdir(parents=True, exist_ok=True)

        expected_shape = (int(self.n_per_class), 17, int(self.expected_n_samples))

        if meta_path.exists():
            meta = json.loads(meta_path.read_text(encoding="utf-8"))
            if tuple(meta.get("shape", ())) != expected_shape:
                raise RuntimeError(
                    f"LDM cache shape mismatch for class '{class_name}'. "
                    f"Expected {expected_shape}, found {tuple(meta.get('shape', ()))}, "
                    f"cache={str(x_path)}. Delete the cache directory to regenerate."
                )
            if int(meta.get("n_steps", -1)) != int(self._n_steps_by_class[class_name]):
                raise RuntimeError(
                    f"LDM cache steps mismatch for class '{class_name}'. "
                    f"Expected n_steps={int(self._n_steps_by_class[class_name])}, found {int(meta.get('n_steps', -1))}, "
                    f"cache={str(x_path)}. Delete the cache directory to regenerate."
                )
            if int(meta.get("seed", -1)) != int(self.seed):
                raise RuntimeError(
                    f"LDM cache seed mismatch for class '{class_name}'. "
                    f"Expected seed={int(self.seed)}, found {int(meta.get('seed', -1))}, "
                    f"cache={str(x_path)}. Delete the cache directory to regenerate."
                )
            if bool(meta.get("normalize", False)) != bool(self.normalize):
                raise RuntimeError(
                    f"LDM cache normalize mismatch for class '{class_name}'. "
                    f"Expected normalize={bool(self.normalize)}, found {bool(meta.get('normalize', False))}, "
                    f"cache={str(x_path)}. Delete the cache directory to regenerate."
                )
        else:
            meta = {
                "class_name": str(class_name),
                "shape": list(expected_shape),
                "dtype": "float16",
                "done_dtype": "uint8",
                "n_steps": int(self._n_steps_by_class[class_name]),
                "seed": int(self.seed),
                "normalize": bool(self.normalize),
            }
            meta_path.write_text(json.dumps(meta, indent=2, sort_keys=True), encoding="utf-8")

        x_mm = np.lib.format.open_memmap(
            str(x_path),
            mode="r+" if x_path.exists() else "w+",
            dtype=np.float16,
            shape=expected_shape,
        )
        done_mm = np.lib.format.open_memmap(
            str(done_path),
            mode="r+" if done_path.exists() else "w+",
            dtype=np.uint8,
            shape=(int(self.n_per_class),),
        )

        self._cache_x[class_name] = x_mm
        self._cache_done[class_name] = done_mm

    def _item_seed(self, *, class_i: int, within_i: int) -> int:
        return int(self.seed) + 1337 * (int(class_i) + 1) + int(within_i)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        if idx < 0 or idx >= len(self):
            raise IndexError(idx)

        class_i = int(idx) // self.n_per_class
        within = int(idx) % self.n_per_class
        class_name = self.classes[class_i]

        if self.enable_cache:
            self._ensure_cache_for_class(class_name)
            done = self._cache_done[class_name]
            if int(done[within]) == 1:
                x_np = np.asarray(self._cache_x[class_name][within], dtype=np.float16)
                x = torch.from_numpy(x_np.astype(np.float32, copy=False))
                y = torch.zeros((len(self.label_names),), dtype=torch.float32)
                y[self.label_to_idx[class_name]] = 1.0
                return x, y

        self._ensure_models_loaded()
        assert self._models is not None

        item_seed = self._item_seed(class_i=class_i, within_i=within)
        torch.manual_seed(item_seed)
        if self.device.type == "cuda":
            torch.cuda.manual_seed_all(item_seed)

        model = self._models[class_name]

        n_steps = int(self._n_steps_by_class[class_name])

        with torch.inference_mode():
            if self.device.type == "cuda":
                with torch.autocast(device_type="cuda", dtype=torch.float16):
                    x_b = model.generate(batch_size=1, device=self.device, num_inference_steps=n_steps)
            else:
                x_b = model.generate(batch_size=1, device=self.device, num_inference_steps=n_steps)

        x = x_b[0].to(dtype=torch.float32, device="cpu")

        if self.normalize:
            x = _zscore_per_channel(x)

        if self.enable_cache:
            self._ensure_cache_for_class(class_name)
            x_mm = self._cache_x[class_name]
            done_mm = self._cache_done[class_name]
            x_mm[within] = x.to(dtype=torch.float16).numpy(force=True)
            done_mm[within] = 1
            x_mm.flush()
            done_mm.flush()

        y = torch.zeros((len(self.label_names),), dtype=torch.float32)
        y[self.label_to_idx[class_name]] = 1.0

        return x, y


def build_major_class_ldm_augmentation(
    *,
    real_train_ds: object,
    ldm_root: Path | str,
    major_classes: Sequence[str],
    n_per_class: int,
    sfreq: float,
    window_sec: float,
    normalize: bool,
    seed: int,
    device: torch.device,
    num_inference_steps_override: int | None,
) -> AugmentedTrainDataset:
    """Factory that wraps the provided training dataset with major-class LDM samples."""

    if not hasattr(real_train_ds, "label_names") or not hasattr(real_train_ds, "label_to_idx"):
        raise TypeError("real_train_ds must expose label_names and label_to_idx")
    if not hasattr(real_train_ds, "_index"):
        raise TypeError("real_train_ds must expose _index (window index with label_mask)")

    label_names = tuple(getattr(real_train_ds, "label_names"))
    label_to_idx = dict(getattr(real_train_ds, "label_to_idx"))

    synthetic_ds = LDMSyntheticMajorClassDataset(
        ldm_root=ldm_root,
        classes=major_classes,
        n_per_class=int(n_per_class),
        label_names=label_names,
        expected_sfreq=float(sfreq),
        expected_window_sec=float(window_sec),
        normalize=bool(normalize),
        seed=int(seed),
        device=device,
        num_inference_steps_override=num_inference_steps_override,
    )

    masks: List[int] = []
    for cn in (str(c).strip().lower() for c in major_classes):
        if cn not in label_to_idx:
            raise ValueError(f"Major class '{cn}' not in training label_names")
        bit = 1 << int(label_to_idx[cn])
        masks.extend([bit] * int(n_per_class))

    return AugmentedTrainDataset(
        real_ds=real_train_ds,
        synthetic_ds=synthetic_ds,
        label_names=label_names,
        synthetic_label_masks=masks,
        real_index=list(getattr(real_train_ds, "_index")),
        label_to_idx=label_to_idx,
    )
